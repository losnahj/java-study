package ch03.sec12;

public class CalculationDirectionExample {
    public static void main(String[] args) {
        int var1 = 10;
        int var2 = 20;
        int var3 = 15;

        // + 또는 - 연산의 경우 연산 방향은 오른쪽에서 왼쪽이다.
        int result = var1 + var2 - var3;

        System.out.println(result);
    }
}
